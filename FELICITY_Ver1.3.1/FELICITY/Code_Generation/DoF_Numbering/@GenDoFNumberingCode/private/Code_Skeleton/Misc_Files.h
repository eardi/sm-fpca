using namespace std;

/***************************************************************************************/
int max_of_two(int& a, int& b)
{
	int max;
	if (a > b)
		max = a;
	else
		max = b;
    return(max);
}
/***************************************************************************************/


/***************************************************************************************/
int max_of_three(int& a, int& b, int& c)
{
	int max = max_of_two(a, b);
	max = max_of_two(max, c);
    return(max);
}
/***************************************************************************************/


/***************************************************************************************/
/* max element in an array */
int max_array(int* a, int num_elements)
{
    int i, max=0;
    for (i=0; i < num_elements; i++)
        {
        if (a[i]>max)
            {
            max=a[i];
            }
        }
    return(max);
}
/***************************************************************************************/


/***************************************************************************************/
/* simple abs */
inline int abs_index(const int& value)
{
    if (value < 0)
		return -value;
	else
		return value;
}
